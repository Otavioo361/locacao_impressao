    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        LtdCons veterinarioEditando = (LtdCons) this.getObjectSelectOnGrid();

        if (veterinarioEditando == null)
            JOptionPane.showMessageDialog(this, "Primeiro selecione um registro na tabela.");
        else {
            this.limparCampos();
            this.habilitarCampos(true);
            this.preencherFormulario(veterinarioEditando);
            this.idLtdConsEditando = veterinarioEditando.getId();
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelarActionPerformed
        idLtdConsEditando = -1;
        this.limparCampos();
        this.habilitarCampos(false);
    }//GEN-LAST:event_btnCancelarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
       LtdCons ltdConsEditando = (LtdCons) this.getObjectSelectOnGrid();

        if (ltdConsEditando == null)
            JOptionPane.showMessageDialog(this, "Primeiro selecione um registro na tabela.");
        else {
            try {
                ltdConsController.excluirVeterinario(ltdConsEditando);

                ltdConsController.atualizarTabela(grdltdCons);
                JOptionPane.showMessageDialog(this, "ExclusÃ£o feita com sucesso!");
            } catch (LtdConsException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage());
            }
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        try {
            if (idLtdConsEditando > 0) {
                ltdConsController.atualizarLtdCons(idLtdConsEditando, edtNome.getText(),
                        Autenticador.textToHash(edtSenha.getText()), edtEmail.getText(), edtCnpj.getText());
                idLtdConsEditando = -1;
            } else {
                ltdConsController.cadastrarLtdCons(edtNome.getText(),
                        Autenticador.textToHash(edtSenha.getText()), edtEmail.getText(), edtCnpj.getText());
            }

            ltdConsController.atualizarTabela(grdltdCons);
            this.habilitarCampos(false);
            this.limparCampos();
        } catch (LtdConsException e) {
            System.err.println(e.getMessage());
            JOptionPane.showMessageDialog(this, e.getMessage());
        } catch (NoSuchAlgorithmException ex) {
            Logger.getLogger(IFrLtdCons.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

