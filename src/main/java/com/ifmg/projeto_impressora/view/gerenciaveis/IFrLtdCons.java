    private Object getObjectSelectOnGrid() {
        int rowCliked = grdltdCons.getSelectedRow();
        Object obj = null;
        if (rowCliked >= 0) {
            obj = grdltdCons.getModel().getValueAt(rowCliked, -1);
        }
        return obj;
    }

