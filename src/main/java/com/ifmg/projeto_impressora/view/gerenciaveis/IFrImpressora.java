    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        Impressora equinoEditando = (Impressora) this.getObjectSelectOnGrid();

        if (equinoEditando == null)
            JOptionPane.showMessageDialog(this, "Primeiro selecione um registro na tabela.");
        else {
            try {
                impressoraController.excluirEquino(equinoEditando);

                impressoraController.atualizarTabela(grdImpressoras);
                JOptionPane.showMessageDialog(this, "Exclusão feita com sucesso!");
            } catch (ImpressoraException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage());
            }
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        Impressora equinoEditando = (Impressora) this.getObjectSelectOnGrid();
        if (equinoEditando == null)
            JOptionPane.showMessageDialog(this, "Primeiro selecione um registro na tabela.");
        else {
            this.limparCampos();
            this.habilitarCampos(true);
            this.preencherFormulario(equinoEditando);
            this.idImpressoraEditando = equinoEditando.getId();
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed

        if(cbxTecnico.getSelectedItem().equals("")){
            System.out.println("RETORNA NADA");
        }
        String prop = String.valueOf(cbxProprietario.getSelectedItem());
        String tecnico = String.valueOf(cbxTecnico.getSelectedItem());
        String local = String.valueOf(cbxLocal.getSelectedItem());
        String ltdCons = String.valueOf(cbxFornecedor.getSelectedItem());

        try {
            if (idImpressoraEditando > 0) {
                impressoraController.atualizarImpressora(
                        idImpressoraEditando, edtNome.getText(), edtMarca.getText(),
                        edtDataFab.getText(), prop, ltdCons, local, tecnico
                );
                idImpressoraEditando = -1;
            } else {
                impressoraController.cadastrarImpressora(
                        edtNome.getText(),  edtMarca.getText(),
                        edtDataFab.getText(), prop, ltdCons, local, tecnico
                );
            }

            impressoraController.atualizarTabela(grdImpressoras);
            this.habilitarCampos(false);
            this.limparCampos();
        } catch (ImpressoraException e) {
            System.err.println(e.getMessage());
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

