    public void localCombobox() {
        cbxLocal.addItem("");
        String[] locaisString = localController.buscarLocalString().split("\n");

        for (String localId : locaisString) {
            cbxLocal.addItem(localId);
        }
    }

    public void proprietarioCombobox() {
        cbxProprietario.addItem("");
        String[] proprietarioIdNomeString = propController.buscarProprietariosString().split("\n");

        for (String idNome : proprietarioIdNomeString) {
            cbxProprietario.addItem(idNome);
        }
    }

    public void fornecedorCombobox() {
        cbxFornecedor.addItem("");
        String[] veterinarioIdNomeString = fornecedorController.buscarLtdsString().split("\n");

        for (String idNome : veterinarioIdNomeString) {
            cbxFornecedor.addItem(idNome);
        }
    }

    public void tecnicoCombobox() {
        cbxTecnico.addItem("");
        String[] veterinarioIdNomeString = tecnicoController.buscarTecnicosString().split("\n");

        for (String idNome : veterinarioIdNomeString) {
            cbxTecnico.addItem(idNome);
        }
    }

    public Integer getIdDoidNome(String idNome) {
        String[] novoIdNome = idNome.split(" - ");
        return Integer.parseInt(novoIdNome[0]);
    }

    public void habilitarCampos(boolean flag) {
        edtNome.setEnabled(flag);       
        edtMarca.setEnabled(flag);
        edtDataFab.setEnabled(flag);
        cbxLocal.setEnabled(flag);
        cbxTecnico.setEnabled(flag);
        cbxFornecedor.setEnabled(flag);
        cbxProprietario.setEnabled(flag);
    }

    public void limparCampos() {
        edtNome.setText("");        
        edtMarca.setText("");
        edtDataFab.setText("");
    }

    public void preencherFormulario(Impressora e) {
        edtNome.setText(e.getNome());        
        edtMarca.setText(e.getMarca());
        edtDataFab.setText(String.valueOf(e.getDataFabricacao()));
        
         if(e.getLocal()!= null){
            cbxLocal.getModel().setSelectedItem(String.valueOf(e.getLocal().getId()));
        }
        
        
        if(e.getProprietario() != null){
            cbxProprietario.getModel().setSelectedItem(e.getProprietario().getId() + " - " + e.getProprietario().getNome());
        }
        if(e.getTecnico()!= null){
            cbxTecnico.getModel().setSelectedItem(e.getTecnico().getId() + " - " + e.getTecnico().getNome());
        }
        
        if(e.getLtdCons()!= null){
            cbxFornecedor.getModel().setSelectedItem(e.getLtdCons().getId() + " - " + e.getLtdCons().getNome());
        }
    }

