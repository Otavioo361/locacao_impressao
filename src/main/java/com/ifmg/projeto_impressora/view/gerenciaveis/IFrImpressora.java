    private Object getObjectSelectOnGrid() {
        int rowCliked = grdImpressoras.getSelectedRow();
        Object obj = null;
        if (rowCliked >= 0) {
            obj = grdImpressoras.getModel().getValueAt(rowCliked, -1);
        }
        return obj;
    }

