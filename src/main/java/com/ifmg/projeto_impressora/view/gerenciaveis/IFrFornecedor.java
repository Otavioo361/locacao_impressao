    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        Fornecedor fornecedorEditando = (Fornecedor) this.getObjectSelectOnGrid();

        if (fornecedorEditando == null)
            JOptionPane.showMessageDialog(this, "Primeiro selecione um registro na tabela.");
        else {
            this.limparCampos();
            this.habilitarCampos(true);
            this.preencherFormulario(fornecedorEditando);
            this.idFornecedorEditando = fornecedorEditando.getId();
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelarActionPerformed
        idFornecedorEditando = -1;
        this.limparCampos();
        this.habilitarCampos(false);
    }//GEN-LAST:event_btnCancelarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        Fornecedor fornecedorEditando = (Fornecedor) this.getObjectSelectOnGrid();

        if (fornecedorEditando == null)
            JOptionPane.showMessageDialog(this, "Primeiro selecione um registro na tabela.");
        else {
            try {
                fornecedorController.excluirFornecedor(fornecedorEditando);

                fornecedorController.atualizarTabela(grdFornecedor);
                JOptionPane.showMessageDialog(this, "Exclus√£o feita com sucesso!");
            } catch (FornecedorException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage());
            }
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        try {
            if (idFornecedorEditando > 0) {
                fornecedorController.atualizarFornecedor(idFornecedorEditando, edtNome.getText(), edtPreco.getText());
                idFornecedorEditando = -1;
            } else {
                fornecedorController.cadastrarFornecedor(edtNome.getText(), edtPreco.getText());
            }

            fornecedorController.atualizarTabela(grdFornecedor);
            this.habilitarCampos(false);
            this.limparCampos();
        } catch (FornecedorException e) {
            System.err.println(e.getMessage());
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void grdFornecedorMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_grdFornecedorMouseClicked
        if (evt.getClickCount() == 2) {
            btnEditarActionPerformed(null);
        }
    }//GEN-LAST:event_grdFornecedorMouseClicked

    private Object getObjectSelectOnGrid() {
        int rowCliked = grdFornecedor.getSelectedRow();
        Object obj = null;
        if (rowCliked >= 0) {
            obj = grdFornecedor.getModel().getValueAt(rowCliked, -1);
        }
        return obj;
    }
    

