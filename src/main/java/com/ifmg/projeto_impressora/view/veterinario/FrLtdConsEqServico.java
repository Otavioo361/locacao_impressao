    private void btnRelacionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRelacionarActionPerformed
        String servico = String.valueOf(cbxServicosAdicionais.getSelectedItem());
        String equino = String.valueOf(cbxEquinos.getSelectedItem());
        
        if(!servico.equals("") && !equino.equals("")){
            try {
                equinoController.relacionarImpressoraServico(equino, servico, edtQuantidade.getText());
            } catch (ImpressoraException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage());
            }
            
            equinoController.atualizarTabelaRelacionamentosServicos(grdEquinosServicosAdicionais);
        }
        
        // Atualizar tabela
    }//GEN-LAST:event_btnRelacionarActionPerformed

