    private void preencherListaFornecedor(){
        Impressora impressoraTab = (Impressora) this.getObjectSelectOnGrid();
        
        List<Fornecedor> eqFornecedores= impressoraTab.getFornecedor();
        
        DefaultListModel delistFornecedor = new DefaultListModel();
        
        for (Fornecedor eqFornecedor : eqFornecedores) {
            delistFornecedor.addElement("Id: " + eqFornecedor.getId()
                    + " | Nome: " + eqFornecedor.getNome());
        }
        
        lstFornecedor.setModel(delistFornecedor);
    }
    
    private void preencherListaServicosAdicionais(){
        Impressora equinoTab = (Impressora) this.getObjectSelectOnGrid();
        
        List<ImpressoraServico> impServicos = equinoTab.getImpressoraServico();
        
        DefaultListModel delistServicos = new DefaultListModel();
        
        for (ImpressoraServico impServico : impServicos) {
            delistServicos.addElement("Id: " + impServico.getServicoAdicional().getId()
                    + " | Nome: " + impServico.getServicoAdicional().getServico()
                    + " | Quantidade: " + impServico.getQtd());
        }
        
        lstServicosAdicional.setModel(delistServicos);
    }

    
    
    private Object getObjectSelectOnGrid() {
        int rowCliked = grdImpressora.getSelectedRow();
        Object obj = null;
        if (rowCliked >= 0) {
            obj = grdImpressora.getModel().getValueAt(rowCliked, -1);
        }
        return obj;
    }

